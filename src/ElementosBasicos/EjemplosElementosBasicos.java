package ElementosBasicos;

public class EjemplosElementosBasicos {
    public static void main(String[] args) {
        System.out.println("Números enteros");
        System.out.println("==============");
        System.out.println(1_000_000_000);
        System.out.println("\n\nCaracteres");
        System.out.println("============");
        System.out.println("Los caracteres se representan como comillas simples");
        System.out.println("Por ejemplo: " + 'a');
        System.out.println("Otro ejemplo: " + '5');
        System.out.println("También puede ser un espacio en blanco " + ' ' + '.');

        System.out.println("\n\nCadenas de caracteres");
        System.out.println("=======================");
        System.out.println(123 + 2 + " 123");
        System.out.println(123 + 2 + "\t123");

    }
}
